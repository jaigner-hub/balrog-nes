//go:build ignore

package main

import (
	"fmt"
	"hash/crc32"
)

func main() {
	// Test 07 expected: "00 N, 01 N, 02 N, 03 N, 04 N, 05 -, 06 -, 07 -, 08 -"
	// Per iter: print_a (iter byte) + print_cc (char byte, 'N'=0x4E or '-'=0x2D)
	data := []byte{
		0x00, 0x4E, 0x01, 0x4E, 0x02, 0x4E, 0x03, 0x4E, 0x04, 0x4E,
		0x05, 0x2D, 0x06, 0x2D, 0x07, 0x2D, 0x08, 0x2D,
	}
	table := crc32.MakeTable(crc32.IEEE)
	fmt.Printf("Expected 07 output: %08X  (test expects 91410411)\n", crc32.Checksum(data, table))

	// What my emulator computed: 2B1F5269. Let's find a byte sequence that matches.
	// What if iter 05 is 'N' and iter 04 is '-' (shifted transition)?
	alt1 := []byte{
		0x00, 0x4E, 0x01, 0x4E, 0x02, 0x4E, 0x03, 0x4E, 0x04, 0x2D,
		0x05, 0x4E, 0x06, 0x2D, 0x07, 0x2D, 0x08, 0x2D,
	}
	fmt.Printf("If iter4=-, iter5=N: %08X (mine is 2B1F5269)\n", crc32.Checksum(alt1, table))

	// What if fewer N's (say 00-03 only)?
	alt2 := []byte{
		0x00, 0x4E, 0x01, 0x4E, 0x02, 0x4E, 0x03, 0x4E, 0x04, 0x2D,
		0x05, 0x2D, 0x06, 0x2D, 0x07, 0x2D, 0x08, 0x2D,
	}
	fmt.Printf("If 00-03 N only     : %08X\n", crc32.Checksum(alt2, table))

	// What if 6 N's?
	alt3 := []byte{
		0x00, 0x4E, 0x01, 0x4E, 0x02, 0x4E, 0x03, 0x4E, 0x04, 0x4E,
		0x05, 0x4E, 0x06, 0x2D, 0x07, 0x2D, 0x08, 0x2D,
	}
	fmt.Printf("If 00-05 N          : %08X\n", crc32.Checksum(alt3, table))
}
